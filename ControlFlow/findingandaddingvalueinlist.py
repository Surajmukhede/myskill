# finding value in list using index method

game = ['football','hockey','tennis','cricket']
game.index('hockey')
print(game)

# adding value in list
# using append method
game = ['football','hockey','tennis','cricket']
game.append('gymnastic')  # it will add value at end 
print(game)

#using insert()  method
game = ['football','hockey','tennis','cricket']

game.insert(2,'golf')
print(game)
